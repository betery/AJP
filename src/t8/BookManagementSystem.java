    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            rs.moveToInsertRow();
            rs.updateString(2, txtName.getText());
            rs.updateString(3, txtAuthor.getText());
            rs.updateInt(4, Integer.parseInt(txtPage.getText()));
            rs.updateInt(5, Integer.parseInt(txtQuantity.getText()));
            Date sqlDate = new Date(txtPublishDate.getDate().getTime());
            rs.updateDate(6, sqlDate);
            rs.insertRow();
            rs.moveToCurrentRow();
            setData();
            txtName.setEditable(false);
            txtAuthor.setEditable(false);
            txtPage.setEditable(false);
            txtQuantity.setEditable(false);
            txtPublishDate.setEnabled(false);
        } catch (SQLException ex) {
            Logger.getLogger(BookManagementSystem.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

