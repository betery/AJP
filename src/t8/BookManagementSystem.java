    private void initBook() {
        String sql = "Select * from Book";
        try {
            Connection cn = Tools.getConn();
            st = cn.createStatement(
                    ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            rs = st.executeQuery(sql);
            if(rs.next()) {
                setData();
            }
        } catch (SQLException ex) {
            Logger.getLogger(BookManagementSystem.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }

    private void setData() {
        try {
            txtID.setText(rs.getString(1));
            txtName.setText(rs.getString(2));
            txtAuthor.setText(rs.getString(3));
            txtPage.setText(rs.getString(4));
            txtQuantity.setText(rs.getString(5));
            txtPublishDate.setDate(rs.getDate(6));
        } catch (SQLException ex) {
            Logger.getLogger(BookManagementSystem.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
