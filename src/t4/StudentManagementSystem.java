    void initClass() {
        cbbClass.removeAllItems();
        cbbClass.addItem(new ClassModal(-1, "All"));
        try (
            Connection cn = Tools.getConn();
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery("Select * from tblClass");            
        ) {
            while(rs.next()) {
                ClassModal c = new ClassModal(rs.getInt(1), rs.getString(2));
                cbbClass.addItem(c);
            }
        } catch (SQLException ex) {
            Logger.getLogger(StudentManagementSystem.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }
    
    void initStudent(String sql) {
        vColumn = new Vector();        
        vData = new Vector();
        Vector vRow;
        
        if (sql == null) {
            sql = "Select s.id, s.Name, s.Score, c.ClassName from Student s, tblClass c " 
                   + " where s.classId = c.id";
        }
        try (
            Connection cn = DriverManager.getConnection(
                    "jdbc:sqlserver://LAB3M055\\SQLEXPRESS:1433;databaseName=StudentDB", "sa", "");
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);            
        ) {
            ResultSetMetaData rsmt = rs.getMetaData();
            for (int i = 1; i <= rsmt.getColumnCount(); i++) {
                vColumn.add(rsmt.getColumnName(i));
            }
            while(rs.next()) {
                vRow = new Vector();
                for (int i = 1; i <= rsmt.getColumnCount(); i++) {
                    vRow.add(rs.getString(i));
                }
                vData.add(vRow);
            }
            jTable1.setModel(new DefaultTableModel(vData, vColumn));
            jTable1.getColumnModel().getColumn(0).setPreferredWidth(10);
            jTable1.getColumnModel().getColumn(1).setPreferredWidth(80);
        } catch (SQLException ex) {
            Logger.getLogger(StudentManagementSystem.class.getName()).log(Level.SEVERE, null, ex);
        }        
    }

