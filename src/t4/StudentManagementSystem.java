    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int index = jTable1.getSelectedRow();
        vData.removeElementAt(index);
//        DefaultTableModel model = new DefaultTableModel(vData, vColumn);
//        jTable1.setModel(model);
        jTable1.setModel(new DefaultTableModel(vData, vColumn));
        Vector vRow = (Vector) vData.get(index);
        String id = vRow.get(0).toString();
        String sql = "Delete Student where id = " + id;
        
        try (
            Connection cn = Tools.getConn();
            Statement st = cn.createStatement();
        ) {
            int rows = st.executeUpdate(sql);
            if(rows > 0) {
                JOptionPane.showMessageDialog(this, "Delete successful");
            } else {
                JOptionPane.showMessageDialog(this, "Delete fail");
            }
        } catch (SQLException ex) {
            Logger.getLogger(StudentManagementSystem.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

