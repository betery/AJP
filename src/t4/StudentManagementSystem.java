    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String sql = "Select s.id, s.Name, s.Score, c.ClassName from Student s, tblClass c " 
                   + " where s.classId = c.id";
        String id = txtID.getText().trim();
        String name = txtName.getText().trim();
        String score = txtScore.getText().trim();
        if (!id.equals("")) {
            sql += " AND s.id = " + id;
        }
        if (!name.equals("")) {
            sql += " AND s.Name like '%" + name + "%'";
        }
        if (!score.equals("")) {
            sql += " AND s.Score >= " + score;
        }
        if (cbbClass.getSelectedIndex() > 0) {
            ClassModal c = (ClassModal) cbbClass.getSelectedItem();
            sql += " AND c.id = " + c.id;
        }
        System.out.println(sql);
        initStudent(sql);
    }//GEN-LAST:event_jButton1ActionPerformed

